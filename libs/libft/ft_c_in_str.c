#include "libft.h"

int	ft_c_in_str(char c, char *s2)
{
	int	i;

	i = 0;
	if (!c || !s2)
		return (0);
	while (s2[i] != '\0')
	{
		if (c == s2[i])
			return (1);
		i++;
	}
	return (0);
}
